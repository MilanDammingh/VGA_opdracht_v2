#include "text.h"
#include "stm32_ub_vga_screen.h"

#include <stdio.h>
#include <stdlib.h>


void draw_text (int x, int y) {

	int letter_A[10][4] = {
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0xff, 0xff, 0x00,  //.@@.
			  0x00, 0xff, 0xff, 0x00,  //.@@.
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0xff, 0xff, 0xff, 0xff,  //@@@@
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0x00, 0x00, 0x00

	};
	for (int i=0; i<10; i++) {
		for (int j=0; j<4; j++) {

			int x_pos = j + x;
			int y_pos = i + y;

			UB_VGA_SetPixel(x_pos, y_pos, letter_A[i][j]);
		}
	}

	int letter_B[10][4] = {
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0x00, 0x00, 0x00,  //....
			  0xff, 0xff, 0xff, 0x00,  //@@@.
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0xff, 0xff, 0xff, 0x00,  //@@@.
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0xff, 0xff, 0xff, 0x00,  //@@@.
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0x00, 0x00, 0x00,  //....

	};
	for (int i=0; i<10; i++) {
		for (int j=0; j<4; j++) {

			int x_pos = j + x;
			int y_pos = i + y;

			UB_VGA_SetPixel(x_pos, y_pos, letter_B[i][j]);
		}
	}


	int letter_C[10][4] = {
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0xff, 0xff, 0xff,  //.@@@
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0xff, 0x00, 0x00, 0x00,  //@...
			  0xff, 0x00, 0x00, 0x00,  //@...
			  0xff, 0x00, 0x00, 0xff,  //@..@
			  0x00, 0xff, 0xff, 0x00,  //.@@.
			  0x00, 0x00, 0x00, 0x00,  //....
			  0x00, 0x00, 0x00, 0x00,  //....

	};
	for (int i=0; i<10; i++) {
		for (int j=0; j<4; j++) {

			int x_pos = j + x;
			int y_pos = i + y;

			UB_VGA_SetPixel(x_pos, y_pos, letter_C[i][j]);
		}
	}


}
